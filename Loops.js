// for loop
for (let i = 0; i < 5; i++) {
    console.log(i); 
  }
  
  // while loop
  let count = 0;
  while (count < 10) {
    console.log(count);
    count++;
  }
  
  // for...of (dùng với array)
  const fruits = ["apple", "banana", "orange"];
  for (const fruit of fruits) {
    console.log(fruit);
  }